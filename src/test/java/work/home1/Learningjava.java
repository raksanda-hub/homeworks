package work.home1;

public class Learningjava {

	public static void main(String[] args) {
		// addition
		int c=3;
		double d=4.2;
		double sum=c+d;
		System.out.println("the total value is"+sum);
		// subtraction
		double sub=d-c;
		System.out.println("the total value is"+sub);
		// multiplication
		double mul=c*d;
		System.out.println("the total product is"+mul);
		//division
		double div=d/c;
		System.out.println("the total divides is"+div);
		//modulus
		double mod=c%d;
		System.out.println("the total remainder is"+mod);
		// increment 
		int q=5;
		q+=3;
		System.out.println("increment by 3 is"+q);
		//decrement 
		int j=7;
		j-=3;
		
		System.out.println("decrement by 3 is"+j);
		//logical and "&&"
		double z=60.5;
		System.out.println(z<19.5&&z>100);
		// logical or "||"
		int z1=5;
		System.out.println(z1<10||z1==5);
		//logical
		int z2=15;
		System.out.println(z2>10||z2<100);
		//logical
		int z3=75;
		System.out.println(z3>69||z3==69);
		//comparison
		int z4=8;
		double z5=4.6;
		System.out.println(z4==z5);
		System.out.println(z4!=z5);
		System.out.println(z4>z5);
		System.out.println(z4>=z5);
		System.out.println(z4<=z5);
		
		
		
		
		
		// TODO Auto-generated method stub

	}

}
